package me.rdokollari.arrays;

/**
 * @author Rizart Dokollari @ rdokollari.me
 * @since May 26, 2014
 */
public class GameEntry {

	private String name;
	private int score;

	/**
	 * @param name
	 * @param score
	 */
	public GameEntry(String name, int score) {
		setName(name);
		setScore(score);
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the score
	 */
	public int getScore() {
		return score;
	}

	/**
	 * @param score
	 *            the score to set
	 */
	public void setScore(int score) {
		this.score = score;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "GameEntry [getName()=" + getName() + ", getScore()="
				+ getScore() + "]";
	}

}
